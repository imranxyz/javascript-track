// null and undefined is kind of same. the value is not defined or empty

console.log(42) // => 42 [number]
console.log(3.14) // => 3.14 [number]

console.log('Hello, Friend') // => Hello, Friend [string]
console.log(true) // true
console.log(false) // false

console.log(null) // null
console.log(undefined) // undefined

console.log([1, 2, 3]) // => [1, 2, 3] => array
let obj = {
    'name': 'Kyle Simpson'
}
console.log(obj['name']) // => Kyle Simpson [object]
